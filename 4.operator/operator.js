// todo Comparison
let a = 10;
let b = 5;

// lebih kecil <
console.log(a < b);

// lebih besar >
console.log(a > b);

// sama ==
console.log(a == b);

// identik ===
console.log(a === b);

// tidak sama !=
console.log(a != b);

// tidak identik
console.log(a !== b);

// kurang atau sama
console.log(a <= b);

// besar atau sama
console.log(a >= b);

//todo Logical
let nilaiA = 10;
let nilaiB = 12;

// And
console.log("&&");
console.log(nilaiA < 15 && nilaiB > 10);
console.log(nilaiA > 15 && nilaiB > 10);

// Or
console.log("||");
console.log(nilaiA < 15 || nilaiB > 10);
console.log(nilaiA > 15 || nilaiB > 10);

// Not
console.log("!");
console.log(!(nilaiA < 15));
console.log(!(nilaiB > 10));
